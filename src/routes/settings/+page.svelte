<script lang="ts">
  // svelte components
  import { Switch } from "$lib/components/ui/switch";
  import { Label } from "$lib/components/ui/label";
  import * as Tabs from "$lib/components/ui/tabs/index.js";
  import { Button } from "$lib/components/ui/button";
  import { PlayCircle } from "lucide-svelte";

  // tables
  import ModelsTable from "$lib/components/ModelsTable.svelte";
  import PatternsTable from "$lib/components/PatternsTable.svelte";
  import SecretsTable from "$lib/components/ApiKeysTable.svelte";
  import BaseUrlTable from "$lib/components/BaseUrlTable.svelte";

  // tauri plugins
  import { invoke } from "@tauri-apps/api/core";
  import { onMount } from "svelte";

  let darkMode = false;
  let notifications = true;
  let isInstalling = false;
  let isInstalled = false;

  onMount(async () => {
    try {
      await invoke("get_fabric_dir");
      isInstalled = true;
    } catch (error) {
      isInstalled = false;
    }
  });
</script>

<div class="p-4">
  <h1 class="text-2xl font-bold mb-4">Settings</h1>

  <Tabs.Root value="general">
    <Tabs.List>
      <Tabs.Trigger value="general">General</Tabs.Trigger>
      <Tabs.Trigger value="models">Models</Tabs.Trigger>
      <Tabs.Trigger value="patterns">Patterns</Tabs.Trigger>
      <Tabs.Trigger value="secrets">API Keys</Tabs.Trigger>
      <Tabs.Trigger value="base-urls">Base URLs</Tabs.Trigger>
    </Tabs.List>
    <Tabs.Content value="general">
      <Button
        variant="outline"
        class="flex items-center gap-2"
        disabled={isInstalling}
        on:click={async () => {
          try {
            isInstalling = true;
            const result = await invoke("install_fabric");
            console.log("Installation successful:", result);
            isInstalled = true;
          } catch (error) {
            console.error("Installation failed:", error);
          } finally {
            isInstalling = false;
          }
        }}
      >
        <PlayCircle size={20} />
        {#if isInstalling}
          Installing...
        {:else}
          {isInstalled ? "Update Fabric" : "Install Fabric"}
        {/if}
      </Button>
      <div class="space-y-4 mt-4">
        <div class="flex items-center space-x-2">
          <Switch id="dark-mode" bind:checked={darkMode} />
          <Label for="dark-mode">Dark Mode</Label>
        </div>

        <div class="flex items-center space-x-2">
          <Switch id="notifications" bind:checked={notifications} />
          <Label for="notifications">Enable Notifications</Label>
        </div>
      </div>
    </Tabs.Content>
    <Tabs.Content value="models">
      <div class="mt-4">
        <ModelsTable />
      </div>
    </Tabs.Content>
    <Tabs.Content value="patterns">
      <div class="mt-4">
        <PatternsTable />
      </div>
    </Tabs.Content>
    <Tabs.Content value="secrets">
      <div class="mt-4">
        <SecretsTable />
      </div>
    </Tabs.Content>
    <Tabs.Content value="base-urls">
      <div class="mt-4">
        <BaseUrlTable />
      </div>
    </Tabs.Content>
  </Tabs.Root>
</div>
